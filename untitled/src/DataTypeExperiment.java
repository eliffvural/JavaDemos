public class DataTypeExperiment {
    public static void main(String[] args) {
        int x=19, y=12, z;
        z=x*y;
        System.out.println("x= "+x+ "y= "+y+ "z= "+z);
        char symbol='?';
        System.out.println(symbol);
        boolean result;
        result=true;
        System.out.println(result);
        result=false;
        System.out.println(result);
    }
}